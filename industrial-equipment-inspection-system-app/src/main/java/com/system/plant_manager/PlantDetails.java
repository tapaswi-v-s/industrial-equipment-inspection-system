    void populate(){
        lblPlant.setText(currentPlant.getName());
        equipments = controller.fetchPlantEquipments(currentPlant.getId());
        populateTable();
    }
     void populateTable(){
        DefaultTableModel model = (DefaultTableModel) tblEquipments.getModel();
        model.setRowCount(0);
        for (Equipment e : equipments) {
            Vector<String> row = new Vector<String>();
            row.add("" + e.getId());
            row.add(e.getTag());
            row.add(e.getEquipmentName());
            row.add(e.getIsWorking() == 1 ? "Yes" : "No");
            row.add(e.getRemark());
            model.addRow(row);
        }
     }

